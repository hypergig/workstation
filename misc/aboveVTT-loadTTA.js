window.SOUNDPADS[ttaKey] = {}

// build a tags dict
var tags = {}
Object.entries(dictionary).forEach(([k, v]) => {
    v.forEach(t => {
        if (!(t in tags)) {
            tags[t] = []
        }
        tags[t].push(k)
    })
})

// if a tag has more than 10 tracks, create a container for it and add all the tracks to it
Object.entries(tags).forEach(([k, v]) => {
    if (v.length >= 10) {
        v.forEach(id => {
            addTTATrack(id, k)
        })
    }
})

// sort the pads
window.SOUNDPADS[ttaKey] = Object.keys(window.SOUNDPADS[ttaKey]).sort().reduce(
    (obj, key) => {
        obj[key + " - " + window.SOUNDPADS[ttaKey][key].length] = window.SOUNDPADS[ttaKey][key]
        return obj;
    },
    {}
);

// create a pad with all tracks at the end
