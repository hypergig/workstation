// Add a track
function addTTATrack(songID, bucket) {
    // capitalize the pad name
    bucket = bucket.charAt(0).toUpperCase() + bucket.slice(1)
    if (!(bucket in window.SOUNDPADS[ttaKey])) {
        window.SOUNDPADS[ttaKey][bucket] = []
    }
    const song = SongList["song_" + songID]
    window.SOUNDPADS[ttaKey][bucket].push({
        "name": song.title,
        "src": song.mp3,
        "loop": true,
        "volume": .5,
    })
}

